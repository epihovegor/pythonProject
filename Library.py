# int - целое число (Например 5)
# float - не целое (Например 4,5)
# bool - если нужно только True или False
# str - символы (Например a,b,c,d)
# a//b - при делении a на b символы // позволяют отбросить знаки после запятой (Например 10/6 выдаст не 1,66666, а просто 1)
# a % b - выводит остаток от деления (Например 10/6, остаток будет 4)
# \n - переносит на новую стоку